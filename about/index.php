<p>The DPLA Library Observatory enables users to scan the collections of the DPLA, learn how those collections relate to each other, and discover resources of particular interest. Through the nested graph, collections are visualized by relative size, format, and type of object, allowing users to discern at a glance the scope and makeup of the DPLA's growing repository. Find an item of interest, and a click takes the user to the original record at its home institution. 
</p>
<p>
Library Observatory not only offers a search application for the DPLA. It is also intended to help us understand how collections are put together, and how an initiative like the DPLA helps libraries, archives, and other institutions talk to one another, learn from one another, and make our common cultural heritage accessible and interoperable for all.
</p>
<p>
The DPLA Library Observatory is a project of <a href="http://metalab.harvard.edu/">metaLAB at Harvard</a>, a multi-disciplinary research collaborative and a project of the Berkman Center for Internet and Society.
